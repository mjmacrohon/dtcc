package com.dtcc.model;

public class Cve {
	private String source;
	private String cveNumber;
	private String cveTitle;
	private String url;
	private String description;
	
		
	public String getSource() {
		return source;
	}
	public void setSource(String source) {
		this.source = source;
	}
	public String getCveNumber() {
		return cveNumber;
	}
	public void setCveNumber(String cveNumber) {
		this.cveNumber = cveNumber;
	}
	public String getCveTitle() {
		return cveTitle;
	}
	public void setCveTitle(String cveTitle) {
		this.cveTitle = cveTitle;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	
	
	
	
}
